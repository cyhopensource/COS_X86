
// PIC 8259 Master and Slave
#define PIC1		0x20		/* IO base address for master PIC */
#define PIC2		0xA0		/* IO base address for slave PIC */
#define PIC1_COMMAND	PIC1
#define PIC1_DATA	(PIC1+1)
#define PIC2_COMMAND	PIC2
#define PIC2_DATA	(PIC2+1)
#define PIC_EOI     0x20

#define ICW1_ICW4	0x01		/* ICW4 (not) needed */
#define ICW1_SINGLE	0x02		/* Single (cascade) mode */
#define ICW1_INTERVAL4	0x04		/* Call address interval 4 (8) */
#define ICW1_LEVEL	0x08		/* Level triggered (edge) mode */
#define ICW1_INIT	0x10		/* Initialization - required! = 0x11 */
 
#define ICW4_8086	0x01		/* 8086/88 (MCS-80/85) mode */
#define ICW4_AUTO	0x02		/* Auto (normal) EOI */
#define ICW4_BUF_SLAVE	0x08		/* Buffered mode/slave */
#define ICW4_BUF_MASTER	0x0C		/* Buffered mode/master */
#define ICW4_SFNM	0x10		/* Special fully nested (not) */

#define PIC1_VECTOR_OFFSET 0x20
#define PIC2_VECTOR_OFFSET 0x28

struct idt_entry_struct{
    unsigned short base_lo;
    unsigned short sel;  // segment selector

    unsigned char always0;
    unsigned char flags;
    unsigned short base_hi;  // upper 16bits addr.
}__attribute__((packed));  // aligne as the byte. more packed.

typedef struct idt_entry_struct idt_entry_t;

struct idt_ptr_struct{
    unsigned short limit;
    unsigned int base;    // the address of the first element.
}__attribute__((packed));

typedef struct idt_ptr_struct idt_ptr_t;

